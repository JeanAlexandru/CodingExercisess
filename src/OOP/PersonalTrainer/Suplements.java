package OOP.PersonalTrainer;

public class Suplements {
    private double vitamin=2;
    private double multiplier=10;

    public Suplements(){

    }

    public Suplements(double vitamin, double multiplier){
        this.vitamin=vitamin;
        this.multiplier=multiplier;
    }

    public double getVitamin() {
        return vitamin;
    }

    public void setVitamin(double vitamin) {
        this.vitamin = vitamin;
    }

    public double getMultiplier() {
        return multiplier;
    }

    public void setMultiplier(double multiplier) {
        this.multiplier = multiplier;
    }
}
